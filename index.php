  <h3>Welcome to the ACM-W</h3>

  <p>
    The Association for Computing Machinery for Women at the University of Washington is a group of students in the department (male and female) dedicated to cultivating a strong supportive community of women within the department of Computer Science &amp; Engineering, to helping members succeed academically, socially and professionally, and to demonstrating the importance and necessity of diversity in the computing world.
  </p>

  <h3>Local Chapter </h3>

  <p>
    Right now, we have about 24% women enrolled in CSE while the University of Washington is closer to 50% overall. Why should we all care about this? Any time you have a discipline that leaves out a significant part of the population, you lose a valuable perspective. While many CSE faculty, staff and students are committed to increasing diversity, our department needs an organized group of students to participate in this effort. Involvment of CSE majors is crucial because students of all ages always want to see and hear firsthand what it means to become a computer scientist. That means we need YOU! Membership in ACM-W is open to all students—male and female—who support the goals of the organization. In no way will this group replace or take away from the great work of our current ACM chapter.
  </p>

  <h3> National Chapter</h3>

  <p>
    The ACM-W is a nation-wide organization that aims to recruit more women into CSE. The following statement is taken from the national ACM-W website:
    <q>The mission of ACM-W is to engage in activities and projects that aim to improve the working and learning environments for women in  computing. This includes promoting activities that result in more equal representation of women in CS such as mentoring or role modelling, monitoring the status of women in industrial and academic computing through the gathering of statistics, providing historical information about women's accomplishments and roles in CS, and serving as a repository of information about programs, documents and policies of concern to women in CS.</q>
  </p>

