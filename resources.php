  <h3>Typical Events</h3>
  
  <h4>Tea Time &amp; Faculty Brown Bag</h4>
  <p>
    This is a super casual event where we set aside a roughly two-hour block during the week (usually in the afternoon) for members to 
    stop by and have a cup of tea (or cider or hot cocoa) with us. This is a great time to casually discuss anything of interest or do 
    things like peer resume review or interview practice problems. A cup of tea and pleasant chat makes everyone's day better!
  </p>

  <h4>Annual Winter Affiliates Fair Dinner</h4>
  <p>
  	This is our largest event and it occurs every January on the day of the Winter Career Fair.
	We invite several companies based on student interest and then let them invite students during the career fair.
	The event runs round-robin style so all students have a chance to talk to all companies in small groups while enjoy dinner.
  </p>
  <h4>Bubble Tea</h4>
  <p>
  	Hosted roughly once a quarter, we all get together to adventure down to the Ave to get Bubble Tea or Froyo and talk about
  	current topics in the Computer Science Department, whether that's what courses to take, or current programming projects.
  </p>

  <h4>Craft Night</h4>
  <p>
  	ACMW-ers get together to create something fun! Past crafts include glass bead necklaces, floppy disk notebooks, and perler bead crafts!
  	As always BYOC [Bring Your Own Craft] is always welcome!
  </p>

  <h4>Canoe Trip</h4>
  <p>
  	Each Spring we head down to the WAC [Waterfront Activitives Center] to canoe around Union Bay. This event is a great opportunity to bond with other
  	ACMW-ers as well as spend some quality time outdoors and away from the CSE basement.
  </p>



