  <h3> ACM-W Office Positions </h3>
  <h4>General duties of all officers</h4>
  <ul>
    <li>Work with recruiters and professionals to pursue common goals</li>
    <li>Plan yearly schedule of speakers and events</li>
    <li>Attend weekly ACMW officer meetings</li>
  </ul>

  <h4>Chair</h4>
  <ul>
    <li>Oversees operations</li>
    <li>Organizes and runs weekly officer meetings</li>
    <li>Maintains vision for ACM-W</li>
    <li>Liaisons to staff/faculty</li>
  </ul>

  <h4>Vice Chair</h4>
  <ul>
    <li>Takes lead on planning large events such as affiliates fair dinner</li>
    <li>Takes lead on organizing weekly interview practice sessions</li>
    <li>Acts as officer mediator</li>
    <li>Facilitates member and officer feedback</li>
    <li>Plans other medium size events</li>
  </ul>

  <h4>Secretary</h4>
  <ul>
    <li>Takes minutes</li>
    <li>Updates website</li>
    <li>Updates blog</li>
    <li>Manages membership</li>
  </ul>

  <h4>Treasurer</h4>
  <ul>
    <li>Maintains annual budget</li>
    <li>Keeps track of spending</li>
    <li>Plans events</li>
    <li>Deposits money</li>
    <li>Manages resources</li>
    <li>Does logistical event planning</li>
  </ul>

  <h4>Public Relations</h4>
  <ul>
    <li>Creates posters for events</li>
    <li>Recruits new people into ACMW</li>
    <li>Plans very small, very quick laid back events and throws small events together weekly</li>
    <li>An extroverted person would make a great candidate!</li>
  </ul>
